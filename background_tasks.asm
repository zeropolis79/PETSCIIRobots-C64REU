	;First let's take care of the timers.  This unit runs
	;every cycle so that it can detect contact with another
	;unit.  But it still needs to count down to terminate
	;So, it uses two timers for a 16-bit value.
	LDX	UNIT
	DEC	UNIT_TIMER_B,X
	LDA	UNIT_TIMER_B,X
