	RTS	

EVILBOT:
	LDX	UNIT
	LDA	#5
	STA	UNIT_TIMER_A,X
	;first animate evilbot
	LDA	UNIT_TILE,X
	CMP	#100
	BNE	EVIL1
	INC	UNIT_TILE,X
	JMP	EVIL10
