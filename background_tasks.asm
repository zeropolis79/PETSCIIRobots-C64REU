	;first tile
	INC	MAP_Y
	JSR	BEX_PART2
	BEQ	BEX1S1
	RTS
BEX1S1:	LDA	TILE
	STA	EXP_BUFFER+2
	JSR	BEX_PART3
	;second tile
	INC	MAP_Y
	JSR	BEX_PART2
	BEQ	BEX1S2
	RTS
BEX1S2:	LDA	TILE
	STA	EXP_BUFFER+3
	JSR	BEX_PART3
	RTS

BEX1_EAST:
	JSR	BEX_PART1
	;first tile
	INC	MAP_X
	JSR	BEX_PART2
	BEQ	BEX1E1
	RTS
BEX1E1:	LDA	TILE
	STA	EXP_BUFFER+4
	JSR	BEX_PART3
	;second tile
	INC	MAP_X
	JSR	BEX_PART2
	BEQ	BEX1E2
