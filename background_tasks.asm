	STA	UNIT_TIMER_B,X
	LDA	#%00000001	;WALK
	STA	MOVE_TYPE
	;CHECK FOR HORIZONTAL MOVEMENT
	LDA	UNIT_LOC_X,X
	CMP	UNIT_LOC_X
	BEQ	EVIL13
	BCC	EVIL12
	JSR	REQUEST_WALK_LEFT
	JMP	EVIL13
EVIL12:	JSR	REQUEST_WALK_RIGHT
EVIL13:	;NOW CHECK FOR VERITCAL MOVEMENT
	LDA	UNIT_LOC_Y,X
	CMP	UNIT_LOC_Y
	BEQ	EVIL20
	BCC	EVIL14
